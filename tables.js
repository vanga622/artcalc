// ====================== ИНТЕРПОЛЯЦИЯ ======================
// Monotonic cubic interpolation (Fritsch-Carlson) — гладкая и без выбросов.
// Для значений «целое число» (прицел П, веера, Z) — округляем результат.
function monotonicCubic(xs, ys, x) {
  const n = xs.length;
  if (n === 0) return NaN;
  if (n === 1) return ys[0];
  const asc = xs[0] <= xs[n - 1];
  // Границы — экстраполяция линейная
  if ((asc && x <= xs[0]) || (!asc && x >= xs[0])) {
    const k = (ys[1] - ys[0]) / (xs[1] - xs[0]);
    return ys[0] + k * (x - xs[0]);
  }
  if ((asc && x >= xs[n - 1]) || (!asc && x <= xs[n - 1])) {
    const k = (ys[n - 1] - ys[n - 2]) / (xs[n - 1] - xs[n - 2]);
    return ys[n - 1] + k * (x - xs[n - 1]);
  }
  // Находим интервал
  let i = 0;
  for (let k = 0; k < n - 1; k++) {
    if ((asc && x >= xs[k] && x <= xs[k + 1]) || (!asc && x <= xs[k] && x >= xs[k + 1])) { i = k; break; }
  }
  // Fritsch-Carlson
  const h = xs[i + 1] - xs[i];
  // Секанские наклоны
  const deltas = [];
  for (let k = 0; k < n - 1; k++) {
    deltas.push((ys[k + 1] - ys[k]) / (xs[k + 1] - xs[k]));
  }
  // Производные в узлах
  const m = new Array(n).fill(0);
  m[0] = deltas[0];
  m[n - 1] = deltas[n - 2];
  for (let k = 1; k < n - 1; k++) {
    if (deltas[k - 1] * deltas[k] <= 0) {
      m[k] = 0;
    } else {
      m[k] = (deltas[k - 1] + deltas[k]) / 2;
    }
  }
  // Ограничение монотонности
  for (let k = 0; k < n - 1; k++) {
    if (deltas[k] === 0) {
      m[k] = 0;
      m[k + 1] = 0;
    } else {
      const a = m[k] / deltas[k];
      const b = m[k + 1] / deltas[k];
      const s = a * a + b * b;
      if (s > 9) {
        const t = 3 / Math.sqrt(s);
        m[k] = t * a * deltas[k];
        m[k + 1] = t * b * deltas[k];
      }
    }
  }
  // Эрмитовский кубический полином
  const t = (x - xs[i]) / h;
  const t2 = t * t;
  const t3 = t2 * t;
  const h00 = 2 * t3 - 3 * t2 + 1;
  const h10 = t3 - 2 * t2 + t;
  const h01 = -2 * t3 + 3 * t2;
  const h11 = t3 - t2;
  return h00 * ys[i] + h10 * h * m[i] + h01 * ys[i + 1] + h11 * h * m[i + 1];
}

// Колонки, которые целые числа (округлять при интерполяции)
const INTEGER_COLS = new Set(['P', 'dXtys', 'dP', 'dPh', 'Z', 'dZw', 'dXw', 'dXt', 'dXh', 'Vb', 'Vd']);
// Колонки с десятичными — округлять до 0.1
const DECIMAL_COLS = new Set(['dXpL', 'dXpU', 'T']);

// Получить все коэффициенты по дальности и заряду (с интерполяцией)
function lookupTable(chargeKey, distance) {
  const table = FIRING_TABLES_2B14[chargeKey];
  if (!table) return null;
  const rows = table.rows;
  const xs = rows.map(r => r[0]);
  const minD = Math.min(xs[0], xs[xs.length - 1]);
  const maxD = Math.max(xs[0], xs[xs.length - 1]);
  const result = {
    charge: chargeKey,
    chargeName: table.name,
    D: distance,
    inRange: distance >= minD && distance <= maxD,
    minD,
    maxD
  };
  for (let c = 1; c < TABLE_COLS.length; c++) {
    const colKey = TABLE_COLS[c].key;
    const ys = rows.map(r => r[c]);
    let v = monotonicCubic(xs, ys, distance);
    if (INTEGER_COLS.has(colKey)) {
      v = Math.round(v);
    } else if (DECIMAL_COLS.has(colKey)) {
      v = Math.round(v * 10) / 10;
    }
    result[colKey] = v;
  }
  return result;
}

